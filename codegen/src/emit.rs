//! Emit Rust source (model / read / write) from the resolved [`ModelIr`].
//! Mirrors the spike's hand-written `generated.rs` + `readwrite.rs` shapes.

use std::fmt::Write as _;

use crate::classify::Kind;
use crate::model_ir::{Field, ModelIr, RefEnum, SimpleEnt};

const HEADER: &str = "// @generated by codegen. DO NOT EDIT.\n// Schema-faithful model + generic 2-pass read + topo write.\n\n";

/// Field Rust type for a `Kind` (+ optional / derivable wrapper). A derivable
/// slot (conditionally `*`) is modeled as `Option<T>` (`None` => `*`).
fn field_type(ir: &ModelIr, f: &Field) -> String {
    let base = kind_type(ir, &f.kind);
    if f.optional || f.derivable {
        format!("Option<{base}>")
    } else {
        base
    }
}

fn kind_type(ir: &ModelIr, k: &Kind) -> String {
    match k {
        Kind::Ref(t) => ir.ref_enums[t].rust.clone(),
        Kind::Vec(inner) => format!("Vec<{}>", kind_type(ir, inner)),
        Kind::Real => "f64".into(),
        Kind::Int => "i64".into(),
        Kind::Bool => "bool".into(),
        Kind::Logical => "Logical".into(),
        Kind::Str => "String".into(),
        Kind::Binary => "String".into(),
        Kind::Enum(a) => ir.enums[a].rust.clone(),
        Kind::MeasureSelect(_) => "MeasureValue".into(),
        Kind::StringSelect(_) => "StringSelectValue".into(),
        Kind::IntSelect(_) => "IntMeasureValue".into(),
    }
}

/// Sanitize a field name into a valid Rust ident (handles `ref`, dup names).
fn field_ident(name: &str, used: &mut std::collections::BTreeMap<String, u32>) -> String {
    let raw = match name {
        "ref" | "type" | "self" | "loop" | "box" | "move" => format!("r#{name}"),
        _ => name.to_string(),
    };
    let n = used.entry(name.to_string()).or_insert(0);
    let out = if *n == 0 { raw } else { format!("{raw}_{n}") };
    *n += 1;
    out
}

/// Build the (ident, field) list for a struct/part, deduping names.
fn idents(fields: &[Field]) -> Vec<(String, &Field)> {
    let mut used = std::collections::BTreeMap::new();
    fields
        .iter()
        .map(|f| (field_ident(&f.name, &mut used), f))
        .collect()
}

