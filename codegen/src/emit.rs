pub fn emit_model(ir: &ModelIr) -> String {
    let mut s = String::from(HEADER);

    // Arena + helpers.
    s.push_str(
        r#"#[derive(Debug)]
pub struct Arena<T> { pub items: Vec<T> }
impl<T> Default for Arena<T> { fn default() -> Self { Arena { items: Vec::new() } } }
impl<T> Arena<T> {
    pub fn push(&mut self, v: T) -> usize { let i = self.items.len(); self.items.push(v); i }
    pub fn get(&self, i: usize) -> &T { &self.items[i] }
}

/// EXPRESS LOGICAL (.T./.F./.U.).
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Logical { True, False, Unknown }
impl Logical {
    pub fn parse(s: &str) -> Option<Self> {
        Some(match s { "T" => Self::True, "F" => Self::False, "U" | "UNKNOWN" => Self::Unknown, _ => return None })
    }
    pub fn token(self) -> &'static str {
        match self { Self::True => ".T.", Self::False => ".F.", Self::Unknown => ".U." }
    }
}

/// select-of-scalars: `Some(type_name)` = typed member `TYPE(v)`; `None` = bare.
/// `value` is int-or-real because EXPRESS `number` members (count_measure,
/// numeric_measure) admit both; the token is preserved (2 stays 2, not 2.0).
#[derive(Debug, Clone, PartialEq)]
pub enum MeasureScalar { Int(i64), Real(f64) }
impl MeasureScalar { pub fn as_f64(&self) -> f64 { match self { Self::Int(i) => *i as f64, Self::Real(r) => *r } } }
#[derive(Debug, Clone, PartialEq)]
pub struct MeasureValue { pub type_name: Option<String>, pub value: MeasureScalar }

/// select-of-named-strings (e.g. SELECT(identifier, message)): `Some(type_name)`
/// = typed member `TYPE('s')`; `None` = bare string.
#[derive(Debug, Clone, PartialEq)]
pub struct StringSelectValue { pub type_name: Option<String>, pub value: String }

/// select-of-named-integers (e.g. SELECT(u_direction_count, v_direction_count)):
/// `Some(type_name)` = typed member `TYPE(2)`; `None` = bare integer. Integer-
/// valued so counts are not widened to reals.
#[derive(Debug, Clone, PartialEq)]
pub struct IntMeasureValue { pub type_name: Option<String>, pub value: i64 }

"#,
    );

    // ENUMs.
    let mut enums: Vec<_> = ir.enums.values().collect();
    enums.sort_by(|a, b| a.rust.cmp(&b.rust));
    for e in enums {
        writeln!(s, "#[derive(Debug, Clone, Copy, PartialEq, Eq)]").unwrap();
        writeln!(s, "pub enum {} {{", e.rust).unwrap();
        for (_, v) in &e.members {
            writeln!(s, "    {v},").unwrap();
        }
        writeln!(s, "}}").unwrap();
        writeln!(s, "impl {} {{", e.rust).unwrap();
        writeln!(
            s,
            "    pub fn parse(s: &str) -> Option<Self> {{ Some(match s {{"
        )
        .unwrap();
        for (tok, v) in &e.members {
            writeln!(s, "        \"{tok}\" => Self::{v},").unwrap();
        }
        writeln!(s, "        _ => return None }}) }}").unwrap();
        writeln!(s, "    pub fn token(self) -> &'static str {{ match self {{").unwrap();
        for (tok, v) in &e.members {
            writeln!(s, "        Self::{v} => \".{tok}.\",").unwrap();
        }
        writeln!(s, "    }} }}\n}}\n").unwrap();
    }

    // Id newtypes.
    for se in &ir.simples {
        writeln!(
            s,
            "#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]\npub struct {}Id(pub usize);",
            se.rust
        )
        .unwrap();
    }
    if ir.has_part_bag {
        writeln!(
            s,
            "#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]\npub struct ComplexUnitId(pub usize);"
        )
        .unwrap();
    }
    s.push('\n');

    // AnyId: one variant per arena (read idmap value).
    writeln!(
        s,
        "#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]\npub enum AnyId {{"
    )
    .unwrap();
    for se in &ir.simples {
        writeln!(s, "    {}({}Id),", se.rust, se.rust).unwrap();
    }
    if ir.has_part_bag {
        writeln!(s, "    ComplexUnit(ComplexUnitId),").unwrap();
    }
    writeln!(s, "}}\n").unwrap();

    // Ref enums.
    let mut refs: Vec<_> = ir.ref_enums.values().collect();
    refs.sort_by(|a, b| a.rust.cmp(&b.rust));
    for re in refs {
        emit_ref_enum(&mut s, re);
    }

    // Simple structs.
    for se in &ir.simples {
        writeln!(
            s,
            "#[derive(Debug, Clone, PartialEq)]\npub struct {} {{",
            se.rust
        )
        .unwrap();
        for (id, f) in idents(&se.fields) {
            if f.derived {
                continue;
            }
            writeln!(s, "    pub {id}: {},", field_type(ir, f)).unwrap();
        }
        writeln!(s, "}}\n").unwrap();
    }

    // Part-bag.
    if ir.has_part_bag {
        writeln!(
            s,
            "#[derive(Debug, Clone, PartialEq)]\npub enum UnitPart {{"
        )
        .unwrap();
        for p in &ir.parts {
            let kept: Vec<_> = idents(&p.fields)
                .into_iter()
                .filter(|(_, f)| !f.derived)
                .collect();
            if kept.is_empty() {
                writeln!(s, "    {},", p.rust).unwrap();
            } else {
                write!(s, "    {} {{ ", p.rust).unwrap();
                for (id, f) in &kept {
                    write!(s, "{id}: {}, ", field_type(ir, f)).unwrap();
                }
                writeln!(s, "}},").unwrap();
            }
        }
        writeln!(s, "}}\n").unwrap();
        writeln!(
            s,
            "#[derive(Debug, Clone, PartialEq)]\npub struct ComplexUnit {{ pub parts: Vec<UnitPart> }}\n"
        )
        .unwrap();
    }

    // Top-level Model.
    writeln!(s, "#[derive(Debug, Default)]\npub struct Model {{").unwrap();
    for se in &ir.simples {
        writeln!(s, "    pub {}: Arena<{}>,", arena_field(&se.rust), se.rust).unwrap();
    }
    if ir.has_part_bag {
        writeln!(s, "    pub complex_units: Arena<ComplexUnit>,").unwrap();
    }
    writeln!(s, "}}").unwrap();

    s
}

/// Payload type for a scalar ref-enum arm. Reuses the select-of-scalars values,
/// which carry `type_name: Option` so the typed (`WIRE(5.)`) vs bare (`5.`) wire
/// form round-trips. (Only Real/Str scalar members occur; others surface here.)
fn scalar_rust_ty(k: &Kind) -> &'static str {
    match k {
        Kind::Real => "f64",
        Kind::Str => "String",
        _ => unreachable!("unsupported scalar arm kind: {k:?}"),
    }
}

fn emit_ref_enum(s: &mut String, re: &RefEnum) {
    // No `Copy` (aggregate arm holds a `Vec`) and no `Eq` (a scalar arm may hold
    // an `f64`, which is not `Eq`). PartialEq/Clone are enough — the model derives
    // only PartialEq. Clone at the few deref sites; compiler flags any missed.
    writeln!(
        s,
        "#[derive(Debug, Clone, PartialEq)]\npub enum {} {{",
        re.rust
    )
    .unwrap();
    for (variant, target) in &re.simple_arms {
        writeln!(s, "    {variant}({target}Id),").unwrap();
    }
    // Aggregate arms for SELECT(.., LIST OF E, SET OF E): hold Vec<{E}Ref>
    // (list/set collapse — wire-identical `(...)`).
    for (variant, elt_ref, _wire) in &re.aggregate {
        writeln!(s, "    {variant}(Vec<{elt_ref}>),").unwrap();
    }
    // Enum arms for SELECT(.., some_enum): hold the (Copy) generated enum; on the
    // wire a `.TOKEN.` literal, discriminated from a `#ref` at read time.
    for (variant, enum_rust, _wire) in &re.enum_arms {
        writeln!(s, "    {variant}({enum_rust}),").unwrap();
    }
    // Scalar arms for SELECT(.., parameter_value): hold the bare scalar value
    // (f64 / String). The wire form is always the typed literal `WIRE(value)`;
    // the form is not stored (write re-derives it from the arm's wire name).
    for (variant, sk, _wire) in &re.scalar_arms {
        writeln!(s, "    {variant}({}),", scalar_rust_ty(sk)).unwrap();
    }
    if re.has_complex {
        writeln!(s, "    Complex(ComplexUnitId),").unwrap();
    }
    // An enum with no arms at all (all ref targets out of closure, e.g. excluded)
    // would be uninhabited and make its referrer unconstructible. Such referrers
    // always escape the round-trip subset, so this variant is a never-resolved,
    // never-emitted placeholder that just keeps the generated code constructible.
    if re.simple_arms.is_empty()
        && re.aggregate.is_empty()
        && re.enum_arms.is_empty()
        && re.scalar_arms.is_empty()
        && !re.has_complex
    {
        writeln!(s, "    Unresolved,").unwrap();
    }
    writeln!(s, "}}").unwrap();
    // resolve from AnyId
    writeln!(s, "impl {} {{", re.rust).unwrap();
    writeln!(s, "    pub fn from_any(a: AnyId) -> Self {{ match a {{").unwrap();
    for (variant, target) in &re.simple_arms {
        writeln!(s, "        AnyId::{target}(i) => Self::{variant}(i),").unwrap();
    }
    if re.has_complex {
        writeln!(s, "        AnyId::ComplexUnit(i) => Self::Complex(i),").unwrap();
    }
    writeln!(
        s,
        "        other => panic!(\"{} ref -> {{other:?}}\"),",
        re.rust
    )
    .unwrap();
    writeln!(s, "    }} }}\n}}\n").unwrap();
}

/// snake_case plural-ish arena field name for a struct rust name.
pub fn arena_field(rust: &str) -> String {
    let mut out = String::new();
    for (i, c) in rust.chars().enumerate() {
        if c.is_uppercase() && i > 0 {
            out.push('_');
        }
        out.push(c.to_ascii_lowercase());
    }
    out.push('s');
    out
}

